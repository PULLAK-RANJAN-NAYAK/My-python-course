# Using the function with an argument 

def greet(name,ending): #Make an function and add two arguments 
    print("Good day ," + name) #Print a message "Good day" and concatenate it with the value of name 
    print(ending) #Print the value of ending parameters

greet("Ranjan","Thankyou") #The value of the parameters are stored here



# Output: 

# Good day ,Ranjan
# Thankyou