# To store multiple data types or things we use either array or list but in python list is  used 

list = ["apple","grape",45,45.66,"repl",False] # As you know string is immutable
print(list[5]) 

list[0] = "mango" # As you can see you can change an string in the list which shows that list is mutable
print(list[0])

print(list[1:4])

print(len(list[0])) # This will give the length of the string in the list