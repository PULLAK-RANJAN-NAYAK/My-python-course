# Using list and tuple to give result

#For loops with lists
l = [1,2,3,4,5,6555,676,890,0]
for i in l:
    print(i)        # print(i) is used to print the content in the index as the i values moves
    
                            
#For loops with tuples
t = (1,2,4,66,78,8990,00)
for i in t:
    print(i)       # Run all the three programs 1 by 1 you will understand
    
    
#For loops with strings
s = "Ranjan"
